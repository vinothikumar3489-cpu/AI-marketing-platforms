import { z } from "zod";
import { prisma } from "../config/prisma.js";
import { generateAnalysis } from "../services/analysis.service.js";

const messageSchema = z.object({
  role: z.enum(["user", "assistant"]),
  content: z.string().min(1),
});

export const addMessage = async (req, res) => {
  const { chatId } = req.params;
  const parseResult = messageSchema.safeParse(req.body);
  if (!parseResult.success) {
    return res.status(400).json({ success: false, error: parseResult.error.errors[0].message });
  }

  const chat = await prisma.chat.findFirst({ where: { id: chatId, userId: req.user.id }, include: { analysis: true } });
  if (!chat) {
    return res.status(404).json({ success: false, error: "Chat not found" });
  }

  const { role, content } = parseResult.data;

  if (role === "assistant") {
    return res.status(400).json({ success: false, error: "Assistant messages must be generated by the system" });
  }

  const userMessage = await prisma.message.create({
    data: { chatId, role, content },
  });

  const analysis = await generateAnalysis({
    productName: chat.productName || "Product Analysis",
    productDescription: chat.analysis?.[0]?.productDescription || content,
    targetAudience: chat.analysis?.[0]?.targetAudience || "Entrepreneurs and marketers",
    followUpQuestion: content,
  });

  const assistantMessage = await prisma.message.create({
    data: {
      chatId,
      role: "assistant",
      content: analysis.message,
      analysisData: analysis.structured,
    },
  });

  return res.json({ userMessage, assistantMessage });
};

export const getMessages = async (req, res) => {
  const { chatId } = req.params;
  const chat = await prisma.chat.findFirst({ where: { id: chatId, userId: req.user.id } });
  if (!chat) {
    return res.status(404).json({ success: false, error: "Chat not found" });
  }

  const messages = await prisma.message.findMany({
    where: { chatId },
    orderBy: { createdAt: "asc" },
  });

  return res.json(messages);
};
